// ─── SMP Script ──────────────────────────────────────────────────────────────

pub fn generate_smp_script(req: &TrainingRequest, dataset_dir: &str, num_classes: usize) -> String {
    let bp = &req.backend_params;
    let model_id = &req.model_id; // e.g. "Unet-resnet34"
    let parts: Vec<&str> = model_id.splitn(2, '-').collect();
    let arch = parts.first().unwrap_or(&"Unet");
    let encoder = if parts.len() > 1 { parts[1] } else { "resnet34" };

    let loss_type = bp.get("loss_type").and_then(|v| v.as_str()).unwrap_or("dice+ce");
    let scheduler = bp.get("scheduler").and_then(|v| v.as_str()).unwrap_or("cosine");
    let encoder_depth = bp.get("encoder_depth").and_then(|v| v.as_u64()).unwrap_or(5);
    let freeze_encoder = bp.get("freeze_encoder").and_then(|v| v.as_bool()).unwrap_or(false);
    let device = format_device(&req.device);

    let dataset_dir_escaped = dataset_dir.replace('\\', "/");

    format!(
        r#"#!/usr/bin/env python3
"""
SMP Training Script - Generated by Annotix
Architecture: {arch} | Encoder: {encoder}
"""
import sys
import json
import os
import numpy as np
from pathlib import Path

def main():
    import torch
    import torch.nn as nn
    from torch.utils.data import Dataset, DataLoader
    import segmentation_models_pytorch as smp
    from PIL import Image
    import albumentations as A
    from albumentations.pytorch import ToTensorV2

    device = torch.device({device} if {device} != "" else ("cuda" if torch.cuda.is_available() else "cpu"))

    dataset_dir = r"{dataset_dir}"
    output_dir = os.path.join(dataset_dir, "train_output")
    os.makedirs(output_dir, exist_ok=True)

    num_classes = {num_classes} + 1  # +1 for background

    # Build model
    model = smp.create_model(
        arch="{arch}",
        encoder_name="{encoder}",
        encoder_weights="imagenet",
        in_channels=3,
        classes=num_classes,
        encoder_depth={encoder_depth},
    )
    if {freeze_encoder}:
        for param in model.encoder.parameters():
            param.requires_grad = False
    model = model.to(device)

    # Loss
    loss_type = "{loss_type}"
    if loss_type == "dice":
        criterion = smp.losses.DiceLoss(mode="multiclass")
    elif loss_type == "ce":
        criterion = nn.CrossEntropyLoss()
    elif loss_type == "focal":
        criterion = smp.losses.FocalLoss(mode="multiclass")
    elif loss_type == "jaccard":
        criterion = smp.losses.JaccardLoss(mode="multiclass")
    else:  # dice+ce
        dice_loss = smp.losses.DiceLoss(mode="multiclass")
        ce_loss = nn.CrossEntropyLoss()
        criterion = lambda pred, target: dice_loss(pred, target) + ce_loss(pred, target)

    # Dataset
    img_size = {imgsz}
    train_transform = A.Compose([
        A.Resize(img_size, img_size),
        A.HorizontalFlip(p=0.5),
        A.RandomBrightnessContrast(p=0.3),
        A.Normalize(),
        ToTensorV2(),
    ])
    val_transform = A.Compose([
        A.Resize(img_size, img_size),
        A.Normalize(),
        ToTensorV2(),
    ])

    class SegDataset(Dataset):
        def __init__(self, images_dir, masks_dir, transform=None):
            self.images = sorted(Path(images_dir).glob("*"))
            self.masks_dir = Path(masks_dir)
            self.transform = transform

        def __len__(self):
            return len(self.images)

        def __getitem__(self, idx):
            img_path = self.images[idx]
            mask_name = img_path.stem + ".png"
            mask_path = self.masks_dir / mask_name
            image = np.array(Image.open(img_path).convert("RGB"))
            mask = np.array(Image.open(mask_path)) if mask_path.exists() else np.zeros(image.shape[:2], dtype=np.uint8)
            if self.transform:
                augmented = self.transform(image=image, mask=mask)
                image, mask = augmented["image"], augmented["mask"]
            return image, mask.long()

    train_ds = SegDataset(os.path.join(dataset_dir, "images/train"), os.path.join(dataset_dir, "masks/train"), train_transform)
    val_ds = SegDataset(os.path.join(dataset_dir, "images/val"), os.path.join(dataset_dir, "masks/val"), val_transform)

    train_loader = DataLoader(train_ds, batch_size={batch}, shuffle=True, num_workers={workers}, pin_memory=True)
    val_loader = DataLoader(val_ds, batch_size={batch}, shuffle=False, num_workers={workers}, pin_memory=True)

    # Optimizer & scheduler
    optimizer = torch.optim.AdamW(filter(lambda p: p.requires_grad, model.parameters()), lr={lr}, weight_decay=1e-4)
    scheduler_type = "{scheduler}"
    if scheduler_type == "cosine":
        scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max={epochs})
    elif scheduler_type == "poly":
        scheduler = torch.optim.lr_scheduler.PolynomialLR(optimizer, total_iters={epochs}, power=0.9)
    else:
        scheduler = torch.optim.lr_scheduler.StepLR(optimizer, step_size=max(1, {epochs}//3), gamma=0.1)

    scaler = torch.amp.GradScaler("cuda") if {amp} and device.type == "cuda" else None

    best_miou = 0.0
    patience_counter = 0

    for epoch in range(1, {epochs} + 1):
        # Train
        model.train()
        train_loss = 0.0
        for images, masks in train_loader:
            images, masks = images.to(device), masks.to(device)
            optimizer.zero_grad()
            if scaler:
                with torch.amp.autocast("cuda"):
                    outputs = model(images)
                    loss = criterion(outputs, masks)
                scaler.scale(loss).backward()
                scaler.step(optimizer)
                scaler.update()
            else:
                outputs = model(images)
                loss = criterion(outputs, masks)
                loss.backward()
                optimizer.step()
            train_loss += loss.item()
        train_loss /= max(len(train_loader), 1)

        # Validate
        model.eval()
        val_loss = 0.0
        intersection = torch.zeros(num_classes, device=device)
        union = torch.zeros(num_classes, device=device)
        correct = torch.zeros(num_classes, device=device)
        total_pixels = torch.zeros(num_classes, device=device)

        with torch.no_grad():
            for images, masks in val_loader:
                images, masks = images.to(device), masks.to(device)
                outputs = model(images)
                loss = criterion(outputs, masks)
                val_loss += loss.item()
                preds = outputs.argmax(dim=1)
                for c in range(num_classes):
                    pred_c = (preds == c)
                    mask_c = (masks == c)
                    intersection[c] += (pred_c & mask_c).sum()
                    union[c] += (pred_c | mask_c).sum()
                    correct[c] += (pred_c & mask_c).sum()
                    total_pixels[c] += mask_c.sum()

        val_loss /= max(len(val_loader), 1)
        iou_per_class = intersection / (union + 1e-6)
        acc_per_class = correct / (total_pixels + 1e-6)
        valid_classes = total_pixels > 0
        mean_iou = iou_per_class[valid_classes].mean().item() if valid_classes.any() else 0.0
        mean_acc = acc_per_class[valid_classes].mean().item() if valid_classes.any() else 0.0

        scheduler.step()
        current_lr = optimizer.param_groups[0]["lr"]

        # Emit metrics
        event = {{
            "type": "epoch",
            "epoch": epoch,
            "totalEpochs": {epochs},
            "progress": (epoch / {epochs}) * 100.0,
            "metrics": {{
                "trainLoss": train_loss,
                "valLoss": val_loss,
                "meanIoU": mean_iou,
                "meanAccuracy": mean_acc,
                "lr": current_lr,
            }},
        }}
        print("ANNOTIX_EVENT:" + json.dumps(event), flush=True)

        # Save best
        if mean_iou > best_miou:
            best_miou = mean_iou
            torch.save(model.state_dict(), os.path.join(output_dir, "best.pth"))
            patience_counter = 0
        else:
            patience_counter += 1
            if {patience} > 0 and patience_counter >= {patience}:
                print(f"Early stopping at epoch {{epoch}}", flush=True)
                break

    # Save last
    torch.save(model.state_dict(), os.path.join(output_dir, "last.pth"))

    # Export ONNX
    try:
        model.eval()
        dummy = torch.randn(1, 3, img_size, img_size).to(device)
        onnx_path = os.path.join(output_dir, "model.onnx")
        torch.onnx.export(model, dummy, onnx_path, opset_version=17, input_names=["input"], output_names=["output"])
    except Exception as e:
        print(f"ONNX export failed: {{e}}", file=sys.stderr)

    result = {{
        "type": "completed",
        "bestModelPath": os.path.join(output_dir, "best.pth"),
        "lastModelPath": os.path.join(output_dir, "last.pth"),
        "resultsDir": output_dir,
    }}
    print("ANNOTIX_EVENT:" + json.dumps(result), flush=True)

if __name__ == "__main__":
    main()
"#,
        arch = arch,
        encoder = encoder,
        dataset_dir = dataset_dir_escaped,
        device = device,
        num_classes = num_classes,
        encoder_depth = encoder_depth,
        freeze_encoder = py_bool(freeze_encoder),
        loss_type = loss_type,
        imgsz = req.image_size,
        batch = req.batch_size,
        workers = req.workers,
        lr = req.lr,
        scheduler = scheduler,
        epochs = req.epochs,
        amp = py_bool(req.amp),
        patience = req.patience,
    )
}

// ─── HuggingFace Segmentation Script ─────────────────────────────────────────

pub fn generate_hf_seg_script(req: &TrainingRequest, dataset_dir: &str, num_classes: usize) -> String {
    let bp = &req.backend_params;
    let model_checkpoint = &req.model_id; // e.g. "nvidia/mit-b0"
    let do_reduce_labels = bp.get("do_reduce_labels").and_then(|v| v.as_bool()).unwrap_or(false);
    let warmup_ratio = bp.get("warmup_ratio").and_then(|v| v.as_f64()).unwrap_or(0.05);
    let weight_decay = bp.get("weight_decay").and_then(|v| v.as_f64()).unwrap_or(0.01);
    let lr_scheduler_type = bp.get("lr_scheduler_type").and_then(|v| v.as_str()).unwrap_or("cosine");

    let dataset_dir_escaped = dataset_dir.replace('\\', "/");

    format!(
        r#"#!/usr/bin/env python3
"""
HuggingFace Semantic Segmentation Training Script - Generated by Annotix
Model: {model_checkpoint}
"""
import sys
import json
import os
import numpy as np
from pathlib import Path

def main():
    import torch
    from torch.utils.data import Dataset, DataLoader
    from transformers import (
        SegformerForSemanticSegmentation,
        SegformerImageProcessor,
        TrainingArguments,
        Trainer,
    )
    from PIL import Image

    dataset_dir = r"{dataset_dir}"
    output_dir = os.path.join(dataset_dir, "train_output")
    os.makedirs(output_dir, exist_ok=True)

    num_classes = {num_classes} + 1  # +1 for background
    do_reduce_labels = {do_reduce_labels}

    # Load processor
    processor = SegformerImageProcessor.from_pretrained("{model_checkpoint}", do_reduce_labels=do_reduce_labels)

    class SegDataset(Dataset):
        def __init__(self, images_dir, masks_dir, processor):
            self.images = sorted(Path(images_dir).glob("*"))
            self.masks_dir = Path(masks_dir)
            self.processor = processor

        def __len__(self):
            return len(self.images)

        def __getitem__(self, idx):
            img_path = self.images[idx]
            mask_name = img_path.stem + ".png"
            mask_path = self.masks_dir / mask_name
            image = Image.open(img_path).convert("RGB")
            mask = Image.open(mask_path) if mask_path.exists() else Image.new("L", image.size, 0)
            encoded = self.processor(images=image, segmentation_maps=mask, return_tensors="pt")
            for k, v in encoded.items():
                encoded[k] = v.squeeze(0)
            return encoded

    train_ds = SegDataset(
        os.path.join(dataset_dir, "images/train"),
        os.path.join(dataset_dir, "masks/train"),
        processor,
    )
    val_ds = SegDataset(
        os.path.join(dataset_dir, "images/val"),
        os.path.join(dataset_dir, "masks/val"),
        processor,
    )

    # Model
    id2label = {{i: f"class_{{i}}" for i in range(num_classes)}}
    label2id = {{v: k for k, v in id2label.items()}}

    # Read class names from classes.txt if available
    classes_file = os.path.join(dataset_dir, "classes.txt")
    if os.path.exists(classes_file):
        with open(classes_file) as f:
            for line in f:
                parts = line.strip().split(": ", 1)
                if len(parts) == 2:
                    idx, name = int(parts[0]), parts[1]
                    if idx < num_classes:
                        id2label[idx] = name
                        label2id[name] = idx

    model = SegformerForSemanticSegmentation.from_pretrained(
        "{model_checkpoint}",
        num_labels=num_classes,
        id2label=id2label,
        label2id=label2id,
        ignore_mismatched_sizes=True,
    )

    # Training args
    training_args = TrainingArguments(
        output_dir=output_dir,
        num_train_epochs={epochs},
        per_device_train_batch_size={batch},
        per_device_eval_batch_size={batch},
        learning_rate={lr},
        lr_scheduler_type="{lr_scheduler_type}",
        warmup_ratio={warmup_ratio},
        weight_decay={weight_decay},
        eval_strategy="epoch",
        save_strategy="epoch",
        save_total_limit=2,
        load_best_model_at_end=True,
        metric_for_best_model="eval_mean_iou",
        greater_is_better=True,
        fp16={amp} and torch.cuda.is_available(),
        dataloader_num_workers={workers},
        logging_steps=10,
        remove_unused_columns=False,
    )

    # Metrics
    def compute_metrics(eval_pred):
        logits, labels = eval_pred
        # logits shape: (batch, num_classes, H, W)
        if isinstance(logits, tuple):
            logits = logits[0]
        logits = torch.tensor(logits)
        labels = torch.tensor(labels)
        # Upsample logits to label size
        if logits.shape[-2:] != labels.shape[-2:]:
            logits = torch.nn.functional.interpolate(
                logits.float(), size=labels.shape[-2:], mode="bilinear", align_corners=False
            )
        preds = logits.argmax(dim=1)
        # Compute mean IoU
        ious = []
        accs = []
        for c in range(num_classes):
            pred_c = (preds == c)
            label_c = (labels == c)
            inter = (pred_c & label_c).sum().item()
            union = (pred_c | label_c).sum().item()
            total = label_c.sum().item()
            if total > 0:
                ious.append(inter / (union + 1e-6))
                accs.append(inter / (total + 1e-6))
        mean_iou = np.mean(ious) if ious else 0.0
        mean_acc = np.mean(accs) if accs else 0.0
        return {{"mean_iou": mean_iou, "mean_accuracy": mean_acc}}

    # Custom callback for Annotix events
    from transformers import TrainerCallback

    class AnnotixCallback(TrainerCallback):
        def on_epoch_end(self, args, state, control, **kwargs):
            epoch = int(state.epoch)
            total = {epochs}
            metrics = {{"lr": state.log_history[-1].get("learning_rate", None)}} if state.log_history else {{}}
            # Get train loss from recent logs
            for entry in reversed(state.log_history):
                if "loss" in entry:
                    metrics["trainLoss"] = entry["loss"]
                    break
            event = {{
                "type": "epoch",
                "epoch": epoch,
                "totalEpochs": total,
                "progress": (epoch / total) * 100.0,
                "metrics": metrics,
            }}
            print("ANNOTIX_EVENT:" + json.dumps(event), flush=True)

        def on_evaluate(self, args, state, control, metrics=None, **kwargs):
            if metrics:
                epoch = int(state.epoch)
                total = {epochs}
                event = {{
                    "type": "epoch",
                    "epoch": epoch,
                    "totalEpochs": total,
                    "progress": (epoch / total) * 100.0,
                    "metrics": {{
                        "valLoss": metrics.get("eval_loss", None),
                        "meanIoU": metrics.get("eval_mean_iou", None),
                        "meanAccuracy": metrics.get("eval_mean_accuracy", None),
                    }},
                }}
                print("ANNOTIX_EVENT:" + json.dumps(event), flush=True)

    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=train_ds,
        eval_dataset=val_ds,
        compute_metrics=compute_metrics,
        callbacks=[AnnotixCallback()],
    )

    trainer.train()

    # Save best
    trainer.save_model(os.path.join(output_dir, "best"))

    # Export ONNX
    try:
        onnx_path = os.path.join(output_dir, "model.onnx")
        dummy = torch.randn(1, 3, {imgsz}, {imgsz})
        torch.onnx.export(model.cpu(), dummy, onnx_path, opset_version=17, input_names=["input"], output_names=["output"])
    except Exception as e:
        print(f"ONNX export failed: {{e}}", file=sys.stderr)

    result = {{
        "type": "completed",
        "bestModelPath": os.path.join(output_dir, "best"),
        "lastModelPath": None,
        "resultsDir": output_dir,
    }}
    print("ANNOTIX_EVENT:" + json.dumps(result), flush=True)

if __name__ == "__main__":
    main()
"#,
        model_checkpoint = model_checkpoint,
        dataset_dir = dataset_dir_escaped,
        num_classes = num_classes,
        do_reduce_labels = py_bool(do_reduce_labels),
        epochs = req.epochs,
        batch = req.batch_size,
        lr = req.lr,
        lr_scheduler_type = lr_scheduler_type,
        warmup_ratio = warmup_ratio,
        weight_decay = weight_decay,
        amp = py_bool(req.amp),
        workers = req.workers,
        imgsz = req.image_size,
    )
}

// ─── MMSegmentation Config + Script ──────────────────────────────────────────

pub fn generate_mmseg_config(req: &TrainingRequest, dataset_dir: &str, num_classes: usize) -> String {
    let bp = &req.backend_params;
    let model_id = &req.model_id;
    let optimizer_type = bp.get("optimizer_type").and_then(|v| v.as_str()).unwrap_or("AdamW");
    let weight_decay = bp.get("weight_decay").and_then(|v| v.as_f64()).unwrap_or(0.01);
    let warmup_iters = bp.get("warmup_iters").and_then(|v| v.as_u64()).unwrap_or(500);
    let checkpoint_interval = bp.get("checkpoint_interval").and_then(|v| v.as_u64()).unwrap_or(1);
    let crop_size = bp.get("crop_size").and_then(|v| v.as_u64()).unwrap_or(512);
    let lr_schedule = bp.get("lr_schedule").and_then(|v| v.as_str()).unwrap_or("poly");

    let dataset_dir_escaped = dataset_dir.replace('\\', "/");
    let num_classes_with_bg = num_classes + 1;

    let lr_schedule_config = match lr_schedule {
        "cosine" => format!(
            "param_scheduler = [\n    dict(type='LinearLR', start_factor=0.001, by_epoch=False, begin=0, end={warmup_iters}),\n    dict(type='CosineAnnealingLR', by_epoch=True, T_max={epochs}),\n]",
            warmup_iters = warmup_iters, epochs = req.epochs
        ),
        "step" => format!(
            "param_scheduler = [\n    dict(type='LinearLR', start_factor=0.001, by_epoch=False, begin=0, end={warmup_iters}),\n    dict(type='MultiStepLR', by_epoch=True, milestones=[{m1}, {m2}], gamma=0.1),\n]",
            warmup_iters = warmup_iters,
            m1 = (req.epochs as f64 * 0.67) as u32,
            m2 = (req.epochs as f64 * 0.89) as u32,
        ),
        _ => format!(
            "param_scheduler = [\n    dict(type='LinearLR', start_factor=0.001, by_epoch=False, begin=0, end={warmup_iters}),\n    dict(type='PolyLR', by_epoch=True, eta_min=1e-6, power=0.9),\n]",
            warmup_iters = warmup_iters,
        ),
    };

    format!(
        r#"# MMSegmentation Config - Generated by Annotix
_base_ = []

model_name = "{model_id}"

# Dataset
dataset_type = "BaseSegDataset"
data_root = r"{dataset_dir}"
crop_size = ({crop_size}, {crop_size})

train_pipeline = [
    dict(type="LoadImageFromFile"),
    dict(type="LoadAnnotations"),
    dict(type="RandomCrop", crop_size=crop_size),
    dict(type="RandomFlip", prob=0.5),
    dict(type="PackSegInputs"),
]

val_pipeline = [
    dict(type="LoadImageFromFile"),
    dict(type="LoadAnnotations"),
    dict(type="Resize", scale=crop_size),
    dict(type="PackSegInputs"),
]

train_dataloader = dict(
    batch_size={batch},
    num_workers={workers},
    dataset=dict(
        type=dataset_type,
        data_root=data_root,
        data_prefix=dict(img_path="images/train", seg_map_path="masks/train"),
        pipeline=train_pipeline,
    ),
)

val_dataloader = dict(
    batch_size=1,
    num_workers={workers},
    dataset=dict(
        type=dataset_type,
        data_root=data_root,
        data_prefix=dict(img_path="images/val", seg_map_path="masks/val"),
        pipeline=val_pipeline,
    ),
)

val_evaluator = dict(type="IoUMetric", iou_metrics=["mIoU"])

# Optimizer
optim_wrapper = dict(
    type="OptimWrapper",
    optimizer=dict(type="{optimizer_type}", lr={lr}, weight_decay={weight_decay}),
)

# Schedule
train_cfg = dict(type="EpochBasedTrainLoop", max_epochs={epochs}, val_interval=1)
val_cfg = dict(type="ValLoop")
{lr_schedule_config}

# Runtime
default_hooks = dict(
    checkpoint=dict(type="CheckpointHook", interval={checkpoint_interval}),
)

num_classes = {num_classes_with_bg}
"#,
        model_id = model_id,
        dataset_dir = dataset_dir_escaped,
        crop_size = crop_size,
        batch = req.batch_size,
        workers = req.workers,
        optimizer_type = optimizer_type,
        lr = req.lr,
        weight_decay = weight_decay,
        epochs = req.epochs,
        lr_schedule_config = lr_schedule_config,
        checkpoint_interval = checkpoint_interval,
        num_classes_with_bg = num_classes_with_bg,
    )
}

pub fn generate_mmseg_train_script(config_path: &str) -> String {
    format!(
        r#"#!/usr/bin/env python3
"""
MMSegmentation Training Script - Generated by Annotix
"""
import sys
import json
import os

def main():
    from mmengine.config import Config
    from mmengine.runner import Runner
    from mmengine.hooks import Hook

    class AnnotixHook(Hook):
        def after_train_epoch(self, runner):
            epoch = runner.epoch + 1
            total = runner.max_epochs
            progress = (epoch / total) * 100.0

            metrics = {{}}
            if hasattr(runner, 'message_hub'):
                log_scalars = runner.message_hub.log_scalars
                for key in ["loss", "decode.loss_ce", "decode.loss_seg"]:
                    full_key = f"train/{{key}}"
                    if full_key in log_scalars:
                        val = log_scalars[full_key]
                        if hasattr(val, 'current'):
                            val = val.current()
                        if key == "loss":
                            metrics["trainLoss"] = float(val)
                        elif "ce" in key:
                            metrics["segLoss"] = float(val)

            event = {{
                "type": "epoch",
                "epoch": epoch,
                "totalEpochs": total,
                "progress": progress,
                "metrics": metrics,
            }}
            print("ANNOTIX_EVENT:" + json.dumps(event), flush=True)

        def after_val_epoch(self, runner, metrics=None):
            if metrics:
                result_metrics = {{}}
                result_metrics["meanIoU"] = metrics.get("aAcc", None)
                result_metrics["meanAccuracy"] = metrics.get("mAcc", None)
                # mIoU from IoUMetric
                for key in metrics:
                    if "mIoU" in key:
                        result_metrics["meanIoU"] = metrics[key]
                event = {{
                    "type": "epoch",
                    "epoch": runner.epoch + 1,
                    "totalEpochs": runner.max_epochs,
                    "progress": ((runner.epoch + 1) / runner.max_epochs) * 100.0,
                    "metrics": result_metrics,
                }}
                print("ANNOTIX_EVENT:" + json.dumps(event), flush=True)

    config_path = r"{config_path}"
    cfg = Config.fromfile(config_path)

    cfg.work_dir = os.path.join(os.path.dirname(config_path), "train")
    os.makedirs(cfg.work_dir, exist_ok=True)

    runner = Runner.from_cfg(cfg)
    runner.register_hook(AnnotixHook(), priority="LOW")
    runner.train()

    # Find best checkpoint
    best_ckpt = os.path.join(cfg.work_dir, "best_mIoU_epoch_*.pth")
    import glob
    best_files = sorted(glob.glob(best_ckpt))
    best_model = best_files[-1] if best_files else None

    result = {{
        "type": "completed",
        "bestModelPath": best_model,
        "lastModelPath": None,
        "resultsDir": cfg.work_dir,
    }}
    print("ANNOTIX_EVENT:" + json.dumps(result), flush=True)

if __name__ == "__main__":
    main()
"#,
        config_path = config_path.replace('\\', "/"),
    )
}

fn generate_export_section(formats: &[String]) -> String {
    if formats.is_empty() {
        return "# No export formats requested".to_string();
    }

    let mut lines = vec![
        "    # Export model to requested formats".to_string(),
        "    exported = []".to_string(),
        format!("    best = YOLO(best_model) if os.path.exists(best_model) else model"),
    ];

    for fmt in formats {
        lines.push(format!(
            "    try:\n        export_path = best.export(format=\"{fmt}\")\n        exported.append({{\"format\": \"{fmt}\", \"path\": str(export_path)}})\n    except Exception as e:\n        print(f\"Warning: export to {fmt} failed: {{e}}\", file=sys.stderr)",
            fmt = fmt,
        ));
    }

    lines.push("    result[\"exportedModels\"] = exported".to_string());
    lines.join("\n")
}
