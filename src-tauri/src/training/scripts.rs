// ─── RF-DETR Script ──────────────────────────────────────────────────────────

pub fn generate_rfdetr_script(req: &TrainingRequest, dataset_dir: &str) -> String {
    let bp = &req.backend_params;
    let model_class = &req.model_id; // e.g. "RFDETRBase"
    let resolution = bp.get("resolution").and_then(|v| v.as_u64()).unwrap_or(560);
    let lr_encoder = bp.get("lr_encoder").and_then(|v| v.as_f64()).unwrap_or(1e-5);
    let grad_accum = bp.get("grad_accum_steps").and_then(|v| v.as_u64()).unwrap_or(4);
    let use_ema = bp.get("use_ema").and_then(|v| v.as_bool()).unwrap_or(true);
    let weight_decay = bp.get("weight_decay").and_then(|v| v.as_f64()).unwrap_or(0.0001);
    let gradient_checkpointing = bp.get("gradient_checkpointing").and_then(|v| v.as_bool()).unwrap_or(false);

    format!(
        r#"#!/usr/bin/env python3
"""
RF-DETR Training Script - Generated by Annotix
Model: {model_class}
"""
import sys
import json
import os

def main():
    from rfdetr import {model_class}

    model = {model_class}()

    dataset_dir = r"{dataset_dir}"
    output_dir = os.path.join(dataset_dir, "train_output")
    os.makedirs(output_dir, exist_ok=True)

    # Callback for epoch metrics
    def on_epoch_end(metrics_dict):
        epoch = metrics_dict.get("epoch", 0)
        total = {epochs}
        progress = (epoch / total) * 100.0
        metrics = {{
            "trainLoss": metrics_dict.get("train_loss", None),
            "valLoss": metrics_dict.get("test_loss", None),
        }}
        event = {{
            "type": "epoch",
            "epoch": epoch,
            "totalEpochs": total,
            "progress": progress,
            "metrics": metrics,
        }}
        print("ANNOTIX_EVENT:" + json.dumps(event), flush=True)

    if hasattr(model, 'callbacks') and "on_fit_epoch_end" in model.callbacks:
        model.callbacks["on_fit_epoch_end"].append(on_epoch_end)

    model.train(
        dataset_dir=dataset_dir,
        epochs={epochs},
        batch_size={batch},
        resolution={resolution},
        lr={lr},
        lr_encoder={lr_encoder},
        grad_accum_steps={grad_accum},
        use_ema={use_ema},
        weight_decay={weight_decay},
        gradient_checkpointing={gradient_checkpointing},
    )

    # Find best checkpoint
    best_model = None
    for name in ["checkpoint_best_total.pth", "checkpoint_best.pth"]:
        candidate = os.path.join(output_dir, name)
        if os.path.exists(candidate):
            best_model = candidate
            break
    if not best_model:
        # Try current dir
        for name in ["checkpoint_best_total.pth", "checkpoint_best.pth"]:
            if os.path.exists(name):
                best_model = os.path.abspath(name)
                break

    result = {{
        "type": "completed",
        "bestModelPath": best_model,
        "lastModelPath": None,
        "resultsDir": output_dir,
    }}
    print("ANNOTIX_EVENT:" + json.dumps(result), flush=True)

if __name__ == "__main__":
    main()
"#,
        model_class = model_class,
        dataset_dir = dataset_dir.replace('\\', "/"),
        epochs = req.epochs,
        batch = req.batch_size,
        resolution = resolution,
        lr = req.lr,
        lr_encoder = lr_encoder,
        grad_accum = grad_accum,
        use_ema = py_bool(use_ema),
        weight_decay = weight_decay,
        gradient_checkpointing = py_bool(gradient_checkpointing),
    )
}

